#include "libft.h"

int		ft_isspace(int c)
{
	unsigned char	uc;
	bool			r;

	uc = (unsigned char)c;
	r = uc == '\t' || uc == '\n' || uc == '\v' || uc == '\f'
		|| uc == '\r' || uc == ' ';
	return (r);
}
